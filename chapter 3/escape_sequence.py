#--------------------from LLM-----------------------
# 1. \n - Newline: Moves the cursor to the next line
print("Line 1\nLine 2")

# 2. \t - Horizontal Tab: Adds a standard indentation space
print("Name:\tAlice\tAge:\t25")

# 3. \\ - Backslash: Prints a literal backslash character
print("Windows Path: C:\\Users\\Name\\Documents")

# 4. \' - Single Quote: Allows single quotes inside a single-quoted string
print('It\'s a beautiful day')

# 5. \" - Double Quote: Allows double quotes inside a double-quoted string
print("He said, \"Python is awesome.\"")

# 6. \r - Carriage Return: Moves cursor to start of current line, overwriting text
print("Hello World\rOver")

# 7. \b - Backspace: Deletes the previous character
print("Errorx\b Corrected")

# 8. \ooo & \xhh - Octal and Hexadecimal values for character representation
print("Hex value for 'A': \x41")
